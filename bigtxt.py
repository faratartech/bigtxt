def print_big_colored(text, color=defaultcolor):
    text = text.upper()
    lines = [""] * 6
    for row in range(6):
        line = ""
        for char in text:
            if char in FONT:
                line += FONT[char][row] + " "
            else:
                line += "       "
        lines[row] = line
    for line in lines:
        console.print(line, style=color)

def parse_arg(arg):
    """Parse text.color format"""
    if "." in arg:
        text, color = arg.rsplit(".", 1)
        return text, color
    return arg, "white"

if __name__ == "__main__":
    if len(sys.argv) > 1:
        for arg in sys.argv[1:]:
            text, color = parse_arg(arg)
            print_big_colored(text, color)
    else:
        user_input = input("Enter text: ")
        print_big_colored(user_input)
